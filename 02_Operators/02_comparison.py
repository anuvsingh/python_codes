a = 13
b = 33

print(a > b)    # False    # a is greater than b
print(a < b)    # True     # a is less than b
print(a == b)   # False    # a is equal to b
print(a != b)   # True     # a is not equal to b
print(a >= b)   # False    # a is greater than equal to b
print(a <= b)   # True     # a is less than equal to b
