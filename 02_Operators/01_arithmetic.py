# Variables
a = 2
b = 3

# Addition
print("Addition:", a + b) # 19 

# Subtraction
print("Subtraction:", a - b) # 11

# Multiplication
print("Multiplication:", a * b)  # 60

# Division
print("Division:", a / b) # 3.75

# Floor Division
print("Floor Division:", a // b)  # 3

# Modulus
print("Modulus:", a % b) # Remainder = 3

# Exponentiation
print("Exponentiation:", a ** (b ** a))   # 2 ** (3 ** 2)














