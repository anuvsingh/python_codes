# AND (&) - Both switches should be ON. Dono he 1 hona chahiyea tbhi answer 1 hoga
a = 15 # 01111
b = 24 # 11000
print (a & b)   # 01000 --> 8

# OR (|) - ON, if any switch is ON. Ek bhi agr 1 hua toh final answer 1 hoga.
n1 = 5 # 0101
n2 = 3 # 0011
print(n1 | n2)    # 0111 --> 7

# XOR (^) - Dono he bits different hone chahiyea tbhi final value 1 hogi.
num1 = 5 # 0101
num2 = 3 # 0011
print(num1 | num2)  # 0110 --> 6

# NOT (~) - Flips the bit.
c = 10 # 1010 --> ~c = -(c + 1)
print(~c) # -6 (0110)

# Left Shift(<<) - 1 bit left - multiplies by 2
print(24<<1) # 110000 --> 48
print(24<<2) # 1100000 --> 96
print(-15<<1) # 

# Right Shift(>>) - 1 bit right - divides by 2
print(19>>1) # 01001
print(-15>>1) # 1111 --> 16 ke niche wala 1 ka matlab negative sign hai --> -7









