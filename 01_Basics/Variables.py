num = 5
name = "Devansh"
dec = 5.012
print(num)
print(name)
print(num, name)
print(num + dec)

# Dynamic Typing - same variable can hold different types of values during execution
num = 10
print(num)

num = "I am Batman"
print(num)

# Multiple Assignments
a = b = c = 10;
print(a)
print(b)
print(c)

x, y, z = 2, 1.02, "Luffy"
print(x, y, z)




